package inheritance;

public class InheritanceDocs {
	
	/*
	 * ----------------- Inheritance ------------------
	 *   # code re-usability
	 *   # process of accessing properties and methods of parent/base/super class
	 *     to child/derived/sub class is known as inheritance.
	 *     
	 *   # Also known as IS-A relationship.
	 *   # Its a Generalization to Specialization process.
	 *   # we can inherit only one class at a time.
	 *   # Multiple inheritance does not support in java with class
	 *     but it happens with interface.
	 *     
	 *   # we cant inherit private properties,methods and constructors.
	 *   # 'extends' keyword is used in inheritance.
	 *   
	 *    # syntax :
	 *    
	 *       child_class  extends parent_class{
	 *            
	 *                //properties : parent + child
	 *                
	 *                //methods   : parent + child
	 *       }
	 *       
	 *       e.g.
	 *       
	 *         class A{
	 *         
	 *         }
	 *         
	 *         
	 *         class B extends A {
	 *         
	 *         }
	 *         
	 *         
	 *         class C extends B {
	 *         
	 *         
	 *         }
	 *         
	 */

}
